# -*- coding: utf-8 -*-
"""
МОДЕЛЬ МОТОРА (плант двойника ЭБУ). НЕЙТРАЛЬ — без нагрузки колёс/дороги.

Идея: это «железо», а блок (прошивка) — «мозг». Они общаются ТОЛЬКО сигналами,
как на живой машине:

   ВХОД в мотор (что делает блок + мы):          ВЫХОД мотора (что видит блок):
     • воздух = винт + РХХ(блок) + газ(мы)   ┐      • показания расходомера (MAF)
     • топливо = впрыск блока                ├──►   • обороты -> период сигналов
     • угол зажигания = блок                 ┘         CAS (частый 1° и опорный 120°)

Мотор ИДЕАЛЬНЫЙ: отвечает ровно так, как блок ОЖИДАЕТ — без реального трения и
разброса. Множитель момента берём из ЯКОРЯ (ожидание блока: столько-то воздуха =
столько-то оборотов на ХХ), а не из головы. Тогда блок «узнаёт свой мотор» и сам
сводит холостой.

Физика на нейтрали — одна связь:
   момент ∝ нагрузка = воздух / обороты   (работа за один такт цилиндра)
   на равновесии: момент = трение -> мотор стоит на оборотах, где воздух «держит» их.
Маховик (инерция) сглаживает: обороты идут к равновесию плавно, не скачком.
"""

AFR_STOICH = 14.7        # стехиометрия (воздух/топливо)


class EngineModel:
    THROTTLE_K = 10.0        # пропускная способность дросселя (см. throttle_airflow)
    WOT_FRAC = 0.70          # с этого открытия — флаг «дроссель открыт» (WOT)

    def __init__(self, idle_rpm=700.0, idle_air=7450.0, inertia=3.0,
                 rpm_min=0.0, rpm_max=7000.0):
        # --- состояние ---
        self.rpm = idle_rpm

        # --- ЯКОРЬ от ожидания блока ---
        # Блок ждёт: при idle_air воздуха мотор крутит idle_rpm. Отсюда и только
        # отсюда — равновесная связь «воздух -> обороты». idle_air later уточним
        # из самого блока (сколько он держит на ХХ), сейчас провизорно.
        self.idle_rpm = idle_rpm
        self.idle_air = idle_air
        self.k = idle_rpm / idle_air          # обороты-равновесие на единицу воздуха

        self.inertia = inertia                # тяжесть маховика (плавность)
        self.rpm_min = rpm_min
        self.rpm_max = rpm_max

    # ----- ФИЗИКА: один шаг -----
    def step(self, air, fuel=None, timing_factor=1.0, sub=1):
        """Прокрутить мотор на один кадр.
        air   — весь воздух, зашедший в мотор (винт+РХХ+газ).
        fuel  — впрыск блока (для AFR/КПД). None = блок льёт по стехио (КПД=1).
        timing_factor — КПД момента от угла (1=оптимальный, <1=позднее зажигание).
        sub   — подшагов интегрирования за кадр (плавность).
        Возвращает текущие обороты.
        """
        eff = self._burn_efficiency(air, fuel)        # КПД сгорания по смеси
        rpm_eq = self.k * air * eff * timing_factor   # равновесные обороты
        for _ in range(max(1, sub)):
            self.rpm += (rpm_eq - self.rpm) / self.inertia
            if self.rpm < self.rpm_min:
                self.rpm = self.rpm_min
            if self.rpm > self.rpm_max:
                self.rpm = self.rpm_max
        return self.rpm

    def _burn_efficiency(self, air, fuel):
        """КПД сгорания: при стехио = 1; бедно/богато — момент падает.
        Если топливо не задано — считаем, что блок попал в стехио (идеал)."""
        if not fuel or air <= 0:
            return 1.0
        afr = air / max(1.0, fuel)
        # грубая колоколом вокруг стехио: дальше от 14.7 — меньше отдача
        d = abs(afr - AFR_STOICH) / AFR_STOICH
        return max(0.0, 1.0 - d)

    # ----- ДРОССЕЛЬ как переменное СУЖЕНИЕ -----
    def throttle_airflow(self, throttle_frac):
        """Дроссель — заслонка, переменное сужение впуска.
        Открытие 0..1 = доля сечения = пропускная СПОСОБНОСТЬ канала.
        Фактический расход растёт И с открытием, И с оборотами — мотор
        «подсасывает» тем сильнее, чем быстрее крутится (как насос):
            расход_дросселя = K · открытие · обороты
        При 0% заслонка закрыта -> через дроссель ~0, воздух только через
        обвод (винт + РХХ). Это и есть холостой ход."""
        thr = max(0.0, min(1.0, throttle_frac))
        return self.THROTTLE_K * thr * self.rpm

    def throttle_switches(self, throttle_frac):
        """Два КОНТАКТА дросселя — реальные сигналы в блок (в TPS две пары):
          • idle_sw (концевик ХХ): замкнут когда дроссель ПОЛНОСТЬЮ закрыт (0%).
                Блок по нему понимает «холостой» -> включает контур РХХ.
          • wot_sw (полный газ):   замкнут когда открыт на WOT_FRAC..100% (70-100%).
                Блок по нему льёт обогащение полной мощности.
        Возвращает (idle_sw, wot_sw) — булевы."""
        thr = max(0.0, min(1.0, throttle_frac))
        idle_sw = thr <= 0.0
        wot_sw = thr >= self.WOT_FRAC
        return idle_sw, wot_sw

    # ----- ВЫХОД 1: расходомер (MAF) -----
    def maf_signal(self, air):
        """Что видит расходомер = ВЕСЬ воздух (винт тоже, мимо TPS)."""
        return int(max(0, min(55000, air)))

    # ----- ВЫХОД 2: сигналы CAS (обороты) -----
    def cas_period(self):
        """Период между опорными метками (120°) для генератора импульсов.
        Чаще импульсы (короче период) = выше обороты — блок так и меряет об."""
        return max(1, round(self.rpm / 1.3))

    def pos_pulses_per_ref(self):
        """Сколько частых 1°-импульсов приходится на один опорный 120°.
        Диск: 360 прорезей (1°) и 6 опорных (120°) -> 60 частых на опорный."""
        return 60

    def state(self):
        return {"rpm": round(self.rpm), "k": round(self.k, 5),
                "idle_anchor": (self.idle_air, self.idle_rpm)}
